package com.chandrika;

public class Command_Line_Argument
{
    public static void main(String[] args)
    {
        String[] agrs = {"A", "B", "C"};
        System.out.println(agrs[0]);
        System.out.println(agrs[1]);
        System.out.println(agrs[2]);
        System.out.println("Length of args : " + agrs.length);

        System.out.println("Square root of 6 : " + Math.sqrt(6));
    }
}
